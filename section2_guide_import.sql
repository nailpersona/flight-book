13. Перед виконанням нічної або змішаної льотної зміни (день-ніч) особам льотних екіпажів та ГКрП надається додатковий відпочинок 4 години (з них не менше 2 годин для сну).

14. Перед виконанням денної льотної зміни у другій половині дня надається додатковий відпочинок для сну 2 години.

15. Льотному складу (за винятком курсантів ВНЗ) та особам ГКрП, які не використали чергову відпустку протягом 12 місяців (тривалістю не менше 24 доби), надається відпустка або вони направляються до профілакторію з метою відновлення функціонального стану та працездатності терміном 10 діб з подальшим допуском до польотів (керівництва польотами) строком до 3-х місяців.

