1) для штатного екіпажу - до 10 годин нальоту при 16 годинах стартового часу;

2) за наявності у складі екіпажу ПС другого льотного екіпажу - до 20 годин нальоту при 24 годинах стартового часу.

9. Максимальний наліт льотчика із застосуванням систем (приладів) нічного бачення в льотну зміну не повинен перевищувати 3 години, в одному польоті - 2 години (на гранично малій висоті - 1 годину).

10. Після досягнення визначеного максимального нальоту чи закінчення стартового часу подальший політ екіпажу ПС дозволяється виконувати після відпочинку (сну) не менше 8 годин.

11. Екіпажам, які виконують тривалі безпосадкові польоти тривалістю більше 12 годин, надається передпольотний відпочинок не менше 10 годин та післяпольотний - не менше доби.

12. Льотному складу та особам ГКрП напередодні дня польотів надається відпочинок для сну не менше 8 годин, а якщо вони брали участь у польотах попередньої зміни (були на чергуванні), перерва між польотами (після чергування) повинна бути не менше 12 годин.

